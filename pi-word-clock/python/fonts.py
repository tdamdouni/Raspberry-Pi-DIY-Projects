def hexString(i):
    return "0x%02x" % i

def printFont(font):
    print ('printing font')
    print ('{')
    for c in sorted(font):
        temp=', '.join(map(hexString,font[c]))
        print ("    '{0}':[{1}],".format(c,temp))
    print ('}')